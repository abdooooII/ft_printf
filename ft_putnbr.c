#include "ft_printf.h"

int	ft_putnbr(long n)
{
	int	len;

	len = ft_countlen(n);
	if (n < 0)
	{
		write(1, "-", 1);
		n = -n;
	}
	if (n > 9)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	else
		ft_putchar(n + '0');
	return (len);
}
